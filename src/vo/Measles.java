
package vo;

public class Measles {
	public int id;
	public int areaid;
	public String areaname;
	public String year;
	public int seak_sum;
	public int dead_sum;
	public String seak_chance;
	public String dead_chance;

	/**
	 * @return the id
	 */
	public int getId() {
		return id;
	}

	/**
	 * @param id
	 *            the id to set
	 */
	public void setId(int id) {
		this.id = id;
	}

	/**
	 * @return the areaid
	 */
	public int getAreaid() {
		return areaid;
	}

	/**
	 * @param areaid
	 *            the areaid to set
	 */
	public void setAreaid(int areaid) {
		this.areaid = areaid;
	}

	/**
	 * @return the areaname
	 */
	public String getAreaname() {
		return areaname;
	}

	/**
	 * @param areaname
	 *            the areanameString to set
	 */
	public void setAreaname(String areaname) {
		this.areaname = areaname;
	}

	/**
	 * @return the year
	 */
	public String getYear() {
		return year;
	}

	/**
	 * @param year
	 *            the year to set
	 */
	public void setYear(String year) {
		this.year = year;
	}

	/**
	 * @return the seak_sum
	 */
	public int getSeak_sum() {
		return seak_sum;
	}

	/**
	 * @param seak_sum
	 *            the seak_sum to set
	 */
	public void setSeak_sum(int seak_sum) {
		this.seak_sum = seak_sum;
	}

	/**
	 * @return the dead_sum
	 */
	public int getDead_sum() {
		return dead_sum;
	}

	/**
	 * @param dead_sum
	 *            the dead_sum to set
	 */
	public void setDead_sum(int dead_sum) {
		this.dead_sum = dead_sum;
	}

	/**
	 * @return the seak_chance
	 */
	public String getSeak_chance() {
		return seak_chance;
	}

	/**
	 * @param seak_chance
	 *            the seak_chance to set
	 */
	public void setSeak_chance(String seak_chance) {
		this.seak_chance = seak_chance;
	}

	/**
	 * @return the dead_chance
	 */
	public String getDead_chance() {
		return dead_chance;
	}

	/**
	 * @param dead_chance
	 *            the dead_chance to set
	 */
	public void setDead_chance(String dead_chance) {
		this.dead_chance = dead_chance;
	}
}
